def write_test_tls_material(directory: Path) -> tuple[Path, Path, Path, Path, Path]:
    """Write deterministic test-only PEM files into an isolated temporary directory."""

    directory.mkdir(parents=True, exist_ok=True)
    certificate_header = "-----BEGIN CERTIFICATE-----"
    certificate_footer = "-----END CERTIFICATE-----"
    key_header = "-----BEGIN " + "PRIVATE KEY-----"
    key_footer = "-----END " + "PRIVATE KEY-----"

    def write_pem(
        name: str,
        der_hex: str,
        header: str,
        footer: str,
        *,
        secret: bool = False,
    ) -> Path:
        encoded = base64.b64encode(bytes.fromhex(der_hex)).decode("ascii")
        lines = [encoded[index : index + 64] for index in range(0, len(encoded), 64)]
        path = directory / name
        path.write_text("\n".join((header, *lines, footer, "")), encoding="ascii")
        if secret and os.name != "nt":
            path.chmod(0o600)
        return path

    server_certificate = write_pem(
        "server.crt",
        SERVER_CERTIFICATE_DER_HEX,
        certificate_header,
        certificate_footer,
    )
    server_private_key = write_pem(
        "server.key",
        SERVER_PRIVATE_KEY_DER_HEX,
        key_header,
        key_footer,
        secret=True,
    )
    ca_certificate = write_pem(
        "ca.crt",
        CA_CERTIFICATE_DER_HEX,
        certificate_header,
        certificate_footer,
    )
    client_certificate = write_pem(
        "client.crt",
        CLIENT_CERTIFICATE_DER_HEX,
        certificate_header,
        certificate_footer,
    )
    client_private_key = write_pem(
        "client.key",
        CLIENT_PRIVATE_KEY_DER_HEX,
        key_header,
        key_footer,
        secret=True,
    )
    return (
        server_certificate,
        server_private_key,
        ca_certificate,
        client_certificate,
        client_private_key,
    )
